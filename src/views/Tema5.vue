      h2 5.1 Estructura del mercado

    p.mb-5(data-aos="fade-right") Al identificar la estructura y la composición del mercado, se determina la forma como se relacionan los oferentes y demandantes en un mercado, el equilibrio de poder, las condiciones de negociación y los determinantes del precio. Desde la economía se han identificado las siguientes formas de mercado:

